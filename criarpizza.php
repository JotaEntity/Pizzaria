    <h4 class="centro">Modo de preparo</h4>
    <div class="pizza">
    <img width="50px" heigth="50px"src="https://www.flaticon.com/svg/static/icons/svg/3717/3717058.svg"></img><br/>
    Dissolva o fermento no leite morno, acrescente os demais ingredientes, menos a farinha.<br/>
    Misture muito bem.<br/>
    Acrescente farinha aos poucos, até soltar das mãos.<br/>
    Sove a massa e deixe descansar enquanto prepara o recheio.<br/>
    Corte os tomates e a cebola em cubos pequenos, tempere a gosto.<br/>
    Divida a massa em 2 partes, abra a massa, cubra com metade do presunto e mussarela e por cima coloque o tomate temperado (escorra bem os tomates para a massa não ficar com aparência de crua).<br/>
    Enrole a massa como rocambole, leve ao forno por aproximadamente 30 minutos.<br/>
